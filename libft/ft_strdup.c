#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t		i;
	char		*str;

	if (ft_strlen(s) == 0)
		return (NULL);
	str = (char *)malloc(sizeof(*s) * (ft_strlen(s) + 1));
	if (!str)
		return (NULL);
	i = 0;
	while (s[i])
	{
		str[i] = s[i];
		i++;
	}
	str[i] = 0;
	return (str);
}
